/**
 * This program demonstrates the use of basic variables to calculate a worker's gross pay
 * 
 * &author Ryan Hickman
 * 
 */

public class Ryan1 
{
	public static void main(String [] args)
	{
		int grossPay;		// Gross pay
		int hours = 40;		// Hours worked
		int payRate = 25;	// Pay rate
		
		grossPay = hours * payRate;								//Calculates the worker's gross pay
		System.out.println("Your grossPay is $" + grossPay);	//Displays the worker's gross pay on the screen
				
			
		
	}
	
	

}

